let slopes = [1, 3, 5, 7, 1];

let trees = [0, 0, 0, 0, 0];

let its = [0, 0, 0, 0, 0];

let lineNum = 0;

for (const line of lines) {
  // check down 1 slopes
  [0, 1, 2, 3].forEach((k) => {
    if (line[its[k] % lines[0].length] == "#") {
      trees[k]++;
    }
  });
  // increment column iterators
  [0, 1, 2, 3].forEach((k) => {
    its[k] += slopes[k];
  });
  // even numbered lines only (down 2)
  if (lineNum % 2 == 0) {
    if (line[its[4] % lines[0].length] == "#") {
      trees[4]++;
    }
    its[4] += slopes[4];
  }
  lineNum++;
}

